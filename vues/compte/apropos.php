</h1>
    <p>
        <strong>EQUITY Bank</strong> L'application est une solution complète pour la gestion des comptes bancaires, offrant une interface conviviale pour les utilisateurs et les administrateurs. Elle permet la création, la gestion et le suivi des comptes, tout en fournissant des outils puissants pour les opérations bancaires courantes.
    </p>
  
        <h3>Fonctionnalités principales :</h3>

<ol>Création de compte bancaire :</ol>

  <p>Permet aux utilisateurs de créer un compte en fournissant des informations personnelles telles que le nom, l'email, le mot de passe, la date de naissance, le type de compte et la devise. <br>
Téléchargement de documents importants comme une photo et une signature pour compléter le processus de création.
    </p>
<ol>Gestion des comptes :</ol>
<p>Visualisation des détails du compte, y compris le numéro de compte, le type de compte, la devise et la date de création <br>
Génération de reçus pour la création de compte, imprimables directement depuis l'application.</p>
<ol>Authentification des administrateurs :</ol>
<p>Accès sécurisé à une interface administrateur avec des fonctionnalités pour la gestion des comptes et la vérification des utilisateurs. <br>
Système d'authentification basé sur les identifiants administratifs pour protéger les fonctionnalités sensibles.</p>
<ol>Interface utilisateur intuitive :</ol>
<p>Utilisation de Bootstrap pour un design responsive et moderne, assurant une expérience utilisateur fluide sur divers appareils. <br>
Formulaires de saisie avec validation intégrée pour garantir la qualité des données et minimiser les erreurs.</p>
<ol>Sécurité et confidentialité :</ol>
<p>
Protection des informations utilisateur avec des techniques de cryptage pour les mots de passe. <br>
Validation des entrées pour prévenir les soumissions incorrectes ou malveillantes.</p>
<ol>Technologies utilisées :</ol>
<p>
Frontend : HTML5, CSS3, Bootstrap <br>
Backend : PHP <br>
Base de données : MySQL <br>
Sécurité : Validation des formulaires, cryptage des mots de passe</p>
